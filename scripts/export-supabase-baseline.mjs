/**
 * Writes `supabase/migrations/*_zarewa_baseline.sql` from the same DDL source as
 * `server/pg/pgMigrate.js` (`buildPostgresBaselineSql` + `splitSqlStatements`).
 *
 * Run after schema changes: `node scripts/export-supabase-baseline.mjs`
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildPostgresBaselineSql, splitSqlStatements } from '../server/pg/pgMigrate.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const migrationsDir = path.join(__dirname, '..', 'supabase', 'migrations');

const HEADER = `/*
 * Zarewa baseline schema — mirrors server/pg/pgMigrate.js ensurePostgresSchema().
 * Generated by scripts/export-supabase-baseline.mjs — do not hand-edit; regenerate after DDL changes.
 *
 * Deploy: \`supabase db push\` (linked project) or apply via Supabase SQL editor.
 * Railway/Express still uses DATABASE_URL + npm run db:migrate until you standardize on one path.
 */

`;

const ZAREWA_META = `CREATE TABLE IF NOT EXISTS zarewa_migrations (
  id TEXT PRIMARY KEY,
  applied_at TIMESTAMPTZ NOT NULL DEFAULT now()
);
`;

function main() {
  fs.mkdirSync(migrationsDir, { recursive: true });

  const baseline = buildPostgresBaselineSql();
  const parts = splitSqlStatements(baseline)
    .map((s) => s.trim())
    .filter(Boolean);

  const body = parts.map((s) => `${s};`).join('\n\n');
  const sql = `${HEADER}${ZAREWA_META}\n${body}\n`;

  const stamp = process.env.ZAREWA_BASELINE_STAMP || '20260415170000';
  const outFile = path.join(migrationsDir, `${stamp}_zarewa_baseline.sql`);

  const existing = fs.readdirSync(migrationsDir).filter((f) => f.endsWith('_zarewa_baseline.sql'));
  for (const f of existing) {
    if (f !== path.basename(outFile)) fs.unlinkSync(path.join(migrationsDir, f));
  }

  fs.writeFileSync(outFile, sql, 'utf8');
  console.log(`[export-supabase-baseline] wrote ${path.relative(path.join(__dirname, '..'), outFile)} (${parts.length} DDL chunks + zarewa_migrations)`);
}

main();
